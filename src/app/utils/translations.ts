export function getItemTypeTranslation(id: number, type: string): string {
  const translation = translations[id];
  if (!translation) {
    return '-';
  }
  switch (type) {
    case 'add-sub-item':
    case 'name-plural':
    case 'new-item':
    case 'name':
      return translation[type];
  }
  return '-';
}

